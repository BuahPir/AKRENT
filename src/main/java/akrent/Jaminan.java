    private void SubmitMobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitMobilActionPerformed
        // TODO add your handling code here:
        if (inputJaminan.getText().equals("Masukkan Jenis Jaminan") || inputUnit.getText().equals("Masukkan Unit")) {
            jLabel7.setForeground(Color.red);
            jLabel7.setText("Error, Pastikan anda memasukkan Semua data");
            
            javax.swing.Timer timer = new javax.swing.Timer(5000, e -> {
                jLabel7.setText(""); // Clear the error message
            });
            timer.setRepeats(false); // Ensure the timer runs only once
            timer.start();
            return;
        }
        String jaminan = inputJaminan.getText();
        String unit = inputUnit.getText();
        try {
            Connection c = KoneksiDatabase.getKoneksi();
            String sql = "INSERT INTO jaminan (jenis_jaminan, unit) VALUES (?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, jaminan);
            p.setString(2, unit);
            p.executeUpdate();
            p.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error: " + e.getMessage());
        } finally {
            loadData(); // Reload data after insertion
            jLabel7.setForeground(Color.green);
            jLabel7.setText("Succes, Telah memasukkan data");
            
            javax.swing.Timer timer = new javax.swing.Timer(5000, e -> {
                jLabel7.setText(""); // Clear the error message
            });
            timer.setRepeats(false); // Ensure the timer runs only once
            timer.start();
            inputJaminan.setText("Masukkan Jenis Jaminan");
            inputJaminan.setForeground(new Color (102,102,102));
            inputUnit.setText("Masukkan Unit");
            inputUnit.setForeground(new Color (102,102,102));
        }
    }//GEN-LAST:event_SubmitMobilActionPerformed

