    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int i = tableRiwayat.getSelectedRow();
        if (i == -1) {
            // tidak ada baris terseleksi
            return;
        }

        String id = model.getValueAt(i, 0).toString();
        try {
            Connection c = KoneksiDatabase.getKoneksi();
            String sql = "DELETE FROM rental WHERE id_rental = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, id);
            p.executeUpdate();
            p.close();
        } catch (SQLException e) {
            System.err.println("Terjadi Error");
        } finally {
            loadData();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (selectedIdRental == null || selectedIdRental.isEmpty()) {
            System.out.println("No row selected. Please select a row to generate the report.");
            return;
        }

        try {
            // Path to the .jrxml file
            File file = new File("src\\main\\java\\akrent\\kontrak.jrxml");

            // Load the design file
            jasperDesign = JRXmlLoader.load(file);

            // Clear any previous parameters and add the selected id_rental as a parameter
            param.clear();
            param.put("id_rental", selectedIdRental);

            // Compile the .jrxml file
            jasperReport = JasperCompileManager.compileReport(jasperDesign);

            // Fill the report with the database connection and parameters
            jasperPrint = JasperFillManager.fillReport(jasperReport, param, KoneksiDatabase.getKoneksi());

            // Display the report
            JasperViewer.viewReport(jasperPrint, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

