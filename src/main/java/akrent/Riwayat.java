    public void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        model.setRowCount(0);

        try {
            Connection c = KoneksiDatabase.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM rental;";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[10];
                o[0] = r.getInt("id_rental");
                o[1] = r.getString("penyewa");
                o[2] = r.getString("jenis_mobil");
                o[3] = r.getInt("harga");
                o[4] = r.getString("tgl_kontrak");
                o[5] = r.getTime("jam_ambil");
                o[6] = r.getTime("jam_kembali");
                o[7] = r.getString("jenis_jaminan");
                o[8] = r.getString("jenis_paket");
                o[9] = r.getInt("lama_sewa");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        } 
    }

